class Gallary extends Component {
  state = {currentImgId: imagesList[0].id}

  getCurrentImg = () => {
    const {currentImgId} = this.state
    const currentImg = imagesList.filter(
      eachItem => eachItem.id === currentImgId,
    )
    return currentImg[0]
  }

  onClickingThumbnail = id => {
    this.setState({currentImgId: id})
  }

  render() {
    const {currentImgId} = this.state
    const currentImg = this.getCurrentImg()
    const {imageUrl, imageAltText} = currentImg
    return (
      <div className="main-container">
        <div className="sub-div">
          <img src={imageUrl} alt={imageAltText} className="img" />
          <div className="text-container">
            <h1 className="heading">Nature Photography</h1>
            <p className="para">Nature Photography by Rahul</p>
            <ul className="unorderedList">
              {imagesList.map(eachItem => (
                <ThumbnailItem
                  eachItem={eachItem}
                  key={eachItem.id}
                  onClickingThumbnail={this.onClickingThumbnail}
                  isActive={currentImgId === eachItem.id}
                />
              ))}
            </ul>
          </div>
        </div>
      </div>
    )
  }
}

export default Gallary
